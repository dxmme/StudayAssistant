const NAV_ITEMS = [
  { href: '/courses', label: 'Kurse', Icon: IconBook },
  { href: '/plan', label: 'Plan', Icon: IconCalendar },
  { href: '/settings', label: 'Einstellungen', Icon: IconSettings },
]

export function NavBar() {
  const pathname = usePathname()

  return (
    <header
      style={{ borderBottom: '1px solid var(--border)', backgroundColor: 'rgba(8,8,8,0.85)' }}
      className="sticky top-0 z-50 backdrop-blur-md"
    >
      <nav className="max-w-3xl mx-auto px-6 h-14 flex items-center justify-between">
        <Link
          href="/"
          className="text-sm font-semibold tracking-widest uppercase"
          style={{ color: 'var(--text)', letterSpacing: '0.12em' }}
        >
          StudyAssistant
        </Link>

        <div className="flex items-center gap-1">
          {NAV_ITEMS.map(({ href, label, Icon }) => {
            const active = pathname === href || pathname.startsWith(href + '/')
            return (
              <Link
                key={href}
                href={href}
                className="flex items-center gap-2 px-3 py-1.5 rounded-md text-sm transition-all"
                style={{
                  color: active ? 'var(--accent)' : 'var(--text-muted)',
                  backgroundColor: active ? 'var(--surface-2)' : 'transparent',
                }}
              >
                <Icon />
                <span>{label}</span>
              </Link>
            )
          })}
        </div>
      </nav>
    </header>
  )
}
